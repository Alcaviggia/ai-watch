const LOCALES: Record<UiLocale, Dict> = { en, fr, nl, es, it, pt, zh, ru };

/** Translation function; falls back to English, then to the key itself. */
export function makeT(locale: UiLocale): (key: string, vars?: Record<string, string | number>) => string {
  return (key, vars) => {
    let out = LOCALES[locale][key] ?? en[key] ?? key;
    if (vars) {
      for (const [k, v] of Object.entries(vars)) out = out.replace(`{${k}}`, String(v));
    }
    return out;
  };
}
