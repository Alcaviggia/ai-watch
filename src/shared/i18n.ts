/**
 * Interface languages — 8 locales, auto-detected from the browser,
 * overridable in the Workshop.
 *
 * House terms are NEVER translated, in any language: The Workshop,
 * Collections, Gravure, Remontoir, Transparence, Calibre, Wrists,
 * Heritage / Studio / Lab. Like any great house.
 *
 * The engraving (the block sent to the AI) is a separate, field-tested
 * concern and stays EN/FR — see settings.injection.blockLanguage.
 */

export const UI_LOCALES = ['en', 'fr', 'nl', 'es', 'it', 'pt', 'zh', 'ru'] as const;
export type UiLocale = (typeof UI_LOCALES)[number];

export const LOCALE_NAMES: Record<UiLocale, string> = {
  en: 'English',
  fr: 'Français',
  nl: 'Nederlands',
  es: 'Español',
  it: 'Italiano',
  pt: 'Português',
  zh: '中文',
  ru: 'Русский',
};

export function resolveLocale(setting: 'auto' | UiLocale, browserLang: string): UiLocale {
  if (setting !== 'auto') return setting;
  const base = browserLang.toLowerCase().split('-')[0] ?? 'en';
  return (UI_LOCALES as readonly string[]).includes(base) ? (base as UiLocale) : 'en';
}

